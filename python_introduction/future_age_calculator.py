# Asks the user for their current age
current_age = int(input("How old are you?"))

# Caculates the amount to add for the difference between today and 2050
years_to_add = 2050 - 2023

# Calculates today's age plus the amount from years_to_add
future_age = current_age + years_to_add

# Prints age in 2050
print("In 2050, you will be ", future_age, "years old.")